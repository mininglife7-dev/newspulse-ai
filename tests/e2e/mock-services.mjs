#!/usr/bin/env node
/**
 * Mock server for E2E tests — stands in for the three external services:
 *
 *   Firecrawl  →  POST /firecrawl/v1/search
 *   OpenAI     →  POST /openai/v1/chat/completions
 *   Supabase   →  /supabase/rest/v1/news_searches (PostgREST subset)
 *
 * The app is pointed here via FIRECRAWL_BASE_URL, OPENAI_BASE_URL and
 * NEXT_PUBLIC_SUPABASE_URL (see playwright.config.ts). Keeps an in-memory
 * `news_searches` table so the full search → save → history flow works.
 */

import { createServer } from 'node:http';
import { randomUUID } from 'node:crypto';

const PORT = Number(process.env.MOCK_PORT || 4545);

/** In-memory news_searches table */
let rows = [];

const ARTICLES = [
  {
    url: 'https://example-news.com/ai-breakthrough',
    title: 'Researchers Announce Major AI Breakthrough',
    description: 'A new model architecture shows strong results.',
    markdown:
      '# AI Breakthrough\nResearchers announced a new architecture today that improves reasoning benchmarks by a wide margin. The team plans to publish code next month.',
    metadata: { publishedTime: '2026-07-08T09:00:00Z' },
  },
  {
    url: 'https://daily-tech.example.org/chips',
    title: 'Chipmakers Race to Meet AI Demand',
    description: 'Foundries expand capacity amid record orders.',
    markdown:
      'Chip manufacturers are expanding production capacity as demand for AI accelerators reaches record levels, with new fabs planned in three countries.',
    metadata: { publishedTime: '2026-07-07T15:30:00Z' },
  },
  {
    url: 'https://world-report.example.net/policy',
    title: 'Governments Debate New AI Rules',
    description: 'Policymakers weigh transparency requirements.',
    markdown:
      'Lawmakers across several regions are debating transparency requirements for AI-generated content, with draft rules expected before the end of the year.',
    metadata: { publishedTime: '2026-07-06T11:00:00Z' },
  },
];

function json(res, status, body, headers = {}) {
  const data = JSON.stringify(body);
  res.writeHead(status, {
    'content-type': 'application/json',
    'content-length': Buffer.byteLength(data),
    ...headers,
  });
  res.end(data);
}

function readBody(req) {
  return new Promise((resolve) => {
    let data = '';
    req.on('data', (c) => (data += c));
    req.on('end', () => {
      try {
        resolve(data ? JSON.parse(data) : {});
      } catch {
        resolve({});
      }
    });
  });
}

const server = createServer(async (req, res) => {
  const url = new URL(req.url, `http://127.0.0.1:${PORT}`);
  const path = url.pathname;

  // --- liveness for playwright webServer ---
  if (path === '/health') return json(res, 200, { ok: true });

  // --- Firecrawl ---
  if (path === '/firecrawl/v1/search' && req.method === 'POST') {
    const body = await readBody(req);
    return json(res, 200, {
      success: true,
      data: ARTICLES.slice(0, Math.min(body.limit ?? 3, ARTICLES.length)),
    });
  }

  // --- OpenAI ---
  if (path === '/openai/v1/chat/completions' && req.method === 'POST') {
    const body = await readBody(req);
    const user = (body.messages || []).find((m) => m.role === 'user');
    const title = /Title: (.*)/.exec(user?.content || '')?.[1] ?? 'article';
    return json(res, 200, {
      id: 'chatcmpl-mock',
      object: 'chat.completion',
      choices: [
        {
          index: 0,
          message: {
            role: 'assistant',
            content: `Mock AI summary of “${title}”: the key facts in two crisp sentences. Generated by the E2E mock, not a real model.`,
          },
          finish_reason: 'stop',
        },
      ],
    });
  }

  // --- Supabase PostgREST subset ---
  if (path === '/supabase/rest/v1/news_searches') {
    const wantsObject = (req.headers.accept || '').includes(
      'vnd.pgrst.object'
    );

    if (req.method === 'POST') {
      const body = await readBody(req);
      const input = Array.isArray(body) ? body[0] : body;
      const row = {
        id: randomUUID(),
        keyword: input.keyword ?? '',
        results: input.results ?? [],
        result_count: input.result_count ?? 0,
        created_at: new Date().toISOString(),
      };
      rows.unshift(row);
      return json(res, 201, wantsObject ? row : [row]);
    }

    if (req.method === 'GET' || req.method === 'HEAD') {
      let out = [...rows];
      const idFilter = url.searchParams.get('id');
      if (idFilter?.startsWith('eq.')) {
        out = out.filter((r) => r.id === idFilter.slice(3));
      }
      const limit = url.searchParams.get('limit');
      if (limit) out = out.slice(0, Number(limit));

      if (req.method === 'HEAD') {
        res.writeHead(200, {
          'content-range': `0-${Math.max(out.length - 1, 0)}/${out.length}`,
        });
        return res.end();
      }
      if (wantsObject) {
        if (out.length === 0) {
          return json(res, 406, {
            code: 'PGRST116',
            message: 'JSON object requested, multiple (or no) rows returned',
          });
        }
        return json(res, 200, out[0], {
          'content-range': `0-0/${out.length}`,
        });
      }
      return json(res, 200, out, {
        'content-range': `0-${Math.max(out.length - 1, 0)}/${out.length}`,
      });
    }

    if (req.method === 'DELETE') {
      const idFilter = url.searchParams.get('id');
      if (idFilter?.startsWith('eq.')) {
        rows = rows.filter((r) => r.id !== idFilter.slice(3));
      } else {
        rows = [];
      }
      res.writeHead(204);
      return res.end();
    }
  }

  json(res, 404, { error: `mock: no route for ${req.method} ${path}` });
});

server.listen(PORT, '127.0.0.1', () => {
  console.log(`[mock-services] listening on http://127.0.0.1:${PORT}`);
});
